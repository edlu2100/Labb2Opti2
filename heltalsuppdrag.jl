nz_idx      = ( var_vals .!= 0 )
var_df      = DataFrames.DataFrame( var=var_names[nz_idx] , value=var_vals[nz_idx] )
println(var_df)
#-------------------------------------------------------------------------------
println(repeat('-',40))
zStar = objective_value(mod)
@printf( " Optimalt målfunktionsvärde:\n  %.3f\n", zStar )
println(repeat('-',40))
