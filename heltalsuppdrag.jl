#-------------------------------------------------------------------------------
# Modellering Heltalsproblem  --  Uppdrag 4
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# Paket och hjälpfunktioner som behövs
#-------------------------------------------------------------------------------
using JuMP, HiGHS, Printf, DataFrames
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# Problemdata
#-------------------------------------------------------------------------------
L = 13      # Maxavstånd till master för varje strategisk punkt
D = 10      # Master inom avstånd D kan inte byggas samtidigt
K =  5      # Maxantal växelstationer per mast
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# DATA MASTER:  f[i]
#-------------------------------------------------------------------------------
MASTER      =  [ "M1", "M2", "M3", "M4", "M5", "M6", "M7", "M8", "M9", "M10"]
xM          =  [   14,   31,    8,   24,   40,   10,   19,   32,   30,   20 ]
yM          =  [   36,   40,   29,   32,   33,   19,   24,   26,   13,    6 ]
f           =  [ 1200, 1100, 1120, 1050, 1220, 1200, 1120, 1090, 1100, 1170 ]
M           =  1:length(MASTER)
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# DATA STATIONER (växelstationer)
#-------------------------------------------------------------------------------
STATIONER   =  ["V1","V2","V3","V4","V5","V6","V7","V8","V9","V10","V11","V12"]
xS          =  [ 22, 39, 17, 26, 39, 14, 21, 28, 36, 16, 24, 37 ]
yS          =  [ 37, 38, 28, 24, 26, 23, 17, 19, 21, 14, 12, 14 ]
S           =  1:length(STATIONER)
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# DATA PUNKTER (strategiska punkter)
#-------------------------------------------------------------------------------
PUNKTER     =  ["A","B","C","D","E" ]
xP          =  [ 19, 28, 23, 18, 32 ]
yP          =  [ 34, 35, 24, 16, 20 ]
P           =  1:length(PUNKTER)
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------------------
# Uträkning av avstånd mellan Master, Växelstationer och Strategiska punkter:
#-------------------------------------------------------------------------------
dist_MS = [ sqrt( (xM[i]-xS[j])^2 + (yM[i]-yS[j])^2 ) for i in M, j in S ]
dist_MM = [ sqrt( (xM[i]-xM[j])^2 + (yM[i]-yM[j])^2 ) for i in M, j in M ]
dist_MP = [ sqrt( (xM[i]-xP[j])^2 + (yM[i]-yP[j])^2 ) for i in M, j in P ]
#-------------------------------------------------------------------------------

#----------------------------------------------
# Beräkning av kostnadsmatrisen c[i,j]
#----------------------------------------------
c = 2*dist_MS
#----------------------------------------------

#-------------------------------------------------------------------------------
# Mängden N[p], master som täcker punkt p:  i in N[p]
#-------------------------------------------------------------------------------
N = [ findall( dist_MP[:,p] .< L )  for p in P ]
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# Mängden I, par av master som står för nära varandra:  (m,n) in I
#-------------------------------------------------------------------------------
I = [ (i,j) for i in M, j in M  if  i < j &&  dist_MM[i,j] <= D ]
#-------------------------------------------------------------------------------


#-----------------  STUDENT PART START  ----------------

#----------------------------------------------------------------
# Model object
#----------------------------------------------------------------
mod = Model(HiGHS.Optimizer)

#----------------------------------------------------------------
# Variables
# (5) x[i] ∈ {0,1}: om mast i byggs
# (6) y[i,j] ∈ {0,1}: om växelstation j kopplas till mast i
#----------------------------------------------------------------
@variable( mod, x[M], Bin )
@variable( mod, y[M, S], Bin )
#----------------------------------------------------------------


#----------------------------------------------------------------
# Objective
# min  Σ_{i∈M} f_i·x_i  +  Σ_{i∈M} Σ_{j∈S} c_ij·y_ij
#----------------------------------------------------------------
@objective( mod, Min,
    sum( f[i] * x[i] for i in M )
    + sum( c[i,j] * y[i,j] for i in M, j in S )
)
#----------------------------------------------------------------


#----------------------------------------------------------------
# Constraints
#----------------------------------------------------------------

# (1) Varje växelstation j kopplas till minst en mast
@constraint( mod, c1[j in S],
    sum( y[i,j] for i in M ) >= 1
)

# (2) Mast i kan bara ta emot kopplingar om den byggs, och då högst K stationer
@constraint( mod, c2[i in M],
    sum( y[i,j] for j in S ) <= K * x[i]
)

# (3) Varje strategisk punkt täcks av minst en byggd mast
@constraint( mod, c3[p in P],
    sum( x[i] for i in N[p] ) >= 1
)

# (4) Förbjudna mastpar – inte båda byggs
@constraint( mod, c4[(i1,i2) in I],
    x[i1] + x[i2] <= 1
)

#----------------------------------------------------------------

#-----------------  STUDENT PART END  ----------------



#-------------------------------------------------------------------------------
# Skriv ut modell-objektet
#-------------------------------------------------------------------------------
println(repeat('-',60))
println("Modellen")
println(repeat('-',60))
print(mod)
println(repeat('-',60))
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------------------
# Lös problemet
#-------------------------------------------------------------------------------
println("\n\n\n>>> LÖS OPTIMERINGSSPROBLEM <<<\n")
optimize!(mod)
#-------------------------------------------------------------------------------


