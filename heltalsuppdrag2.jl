#-------------------------------------------------------------------------------
# Modellering Heltalsproblem  --  Uppdrag 2
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# Paket och hjälpfunktioner som behövs
#-------------------------------------------------------------------------------
using JuMP, HiGHS, Printf, DataFrames
#-------------------------------------------------------------------------------

#-------------------------------------------------------------------------------
# Problemdata
#-------------------------------------------------------------------------------
K   =  6      # max antal anslutna detaljister till ett lager
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------------------
# DATA LAGER:  fast = f[i], admin = k[i], supply = s[i]
#-------------------------------------------------------------------------------
LAGER   =  [ "A", "B", "C", "D", "E", "F", "G", "H", "I", "J", "K", "L", "M" ]
xL      =  [   14,   24,   41,   18,   34,   50,   20,   29,   42,   55,   40,   30,   49 ]
yL      =  [   39,   36,   40,   29,   32,   33,   19,   24,   26,   23,   13,    6,    9 ]
fast    =  [ 1300, 1200, 1100, 1210, 1420, 1320, 1120, 1090, 1280, 1390, 1000, 1170, 1090 ]
admin   =  [    4,    3,    5,    2,    6,    3,    4,    5,    5,    6,    3,    4,    5 ]
supply  =  [   50,   50,   70,   80,   65,   90,   75,   85,   70,   80,   90,   70,   75 ]
L       =  1:length(LAGER)
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------------------
# DATA DETALJISTER:  demand = d[j]
#-------------------------------------------------------------------------------
xD      =  [ 20, 30, 52, 19, 32, 49, 12, 27, 41, 58, 15, 36, 49, 14, 31, 38, 44, 26, 34, 47, 52, 61, 21, 36, 44, 35 ]
yD      =  [ 45, 45, 44, 35, 37, 38, 31, 28, 32, 29, 24, 24, 26, 13, 17, 19, 20, 14, 12, 14, 15, 14, 10,  6,  5,  0 ]
demand  =  [ 20, 20, 12, 19,  8,  9, 12, 17, 13, 15, 22,  9, 18, 13, 17, 19, 20, 14, 13, 14, 16,  6, 12, 16, 14, 17 ]
D       =  1:length(xD)
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------
# Tillåtna kombinationer av lager och detaljister:  KOMB
#-------------------------------------------------------------------
KOMBINATIONER = [
("A", 1), ("A", 2), ("A", 4), ("A", 7), ("A",11),
("B", 1), ("B", 2), ("B", 4), ("B", 5), ("B", 8), ("B",12),
("C", 1), ("C", 2), ("C", 3), ("C", 5), ("C", 6), ("C", 9), ("C",13),
("D", 1), ("D", 2), ("D", 4), ("D", 5), ("D", 7), ("D", 8), ("D",11), ("D",14), ("D",15), 
("E", 1), ("E", 2), ("E", 3), ("E", 4), ("E", 5), ("E", 6), ("E", 8), ("E", 9), ("E",10), ("E",11), ("E",12), 
("E",13), ("E",15), ("E",16), ("E",17),
("F", 3), ("F", 5), ("F", 6), ("F", 9), ("F",10), ("F",12), ("F",13), ("F",17), ("F",20), ("F",21),
("G", 4), ("G", 8), ("G",11), ("G",12), ("G",14), ("G",15), ("G",16), ("G",18), ("G",19), ("G",23), ("G",24), 
("H", 4), ("H", 5), ("H", 7), ("H", 8), ("H", 9), ("H",11), ("H",12), ("H",14), ("H",15), ("H",16), ("H",17), 
("H",18), ("H",19), ("H",20), ("H",21), ("H",23), ("H",26),
("I", 5), ("I", 6), ("I", 8), ("I", 9), ("I",10), ("I",12), ("I",13), ("I",15), ("I",16), ("I",17), 
("I",18), ("I",19), ("I",20), ("I",21), ("I",22), 
("J", 3), ("J", 6), ("J", 9), ("J",10), ("J",12), ("J",13), ("J",16), ("J",17), ("J",20), ("J",21), ("J",22), ("J",25), 
("K",12), ("K",13), ("K",15), ("K",16), ("K",17), ("K",18), ("K",19), ("K",20), ("K",21), ("K",22), ("K",23),
("K",24), ("K",25), ("K",26),
("L",14), ("L",15), ("L",16), ("L",18), ("L",19), ("L",23), ("L",24), ("L",25), ("L",26),
("M",13), ("M",16), ("M",17), ("M",19), ("M",20), ("M",21), ("M",22), ("M",24), ("M",25), ("M",26),
]
#-------------------------------------------------------------------
KOMB = [(i,j) for i in L, j in D if (LAGER[i],j) in KOMBINATIONER ]
#-------------------------------------------------------------------

#-------------------------------------------------------------------------------
# Uträkning av avstånd mellan Lager och Detaljister:  dist_LD = c[i,j]
#-------------------------------------------------------------------------------
dist_LD = [ sqrt( (xL[i]-xD[j])^2 + (yL[i]-yD[j])^2 ) for i in L, j in D ]
#-------------------------------------------------------------------------------

#-----------------  STUDENT PART START  ----------------

#----------------------------------------------------------------
# Model object
#----------------------------------------------------------------
mod = Model(HiGHS.Optimizer)

#----------------------------------------------------------------
# Variables
# om lager i öppnas
# flöde från lager i till detaljist j
# om detaljist j betjänas av lager i
#----------------------------------------------------------------
@variable( mod, y[L], Bin )
@variable( mod, x[KOMB] >= 0, Int )
@variable( mod, z[KOMB], Bin )
#----------------------------------------------------------------


#----------------------------------------------------------------
# Objective
#----------------------------------------------------------------
@objective( mod, Min,
    sum( fast[i]  * y[i]       for i in L )
    + sum( dist_LD[i,j] * x[(i,j)] for (i,j) in KOMB )
    + sum( admin[i] * z[(i,j)] for (i,j) in KOMB )
)
#----------------------------------------------------------------


#----------------------------------------------------------------
# Constraints
#----------------------------------------------------------------

# Efterfrågan uppfylls
@constraint( mod, c1[j in D],
    sum( x[(i,j)] for i in L if (i,j) in KOMB ) == demand[j]
)

# Lagerkapacitet
@constraint( mod, c2[i in L],
    sum( x[(i,j)] for j in D if (i,j) in KOMB ) <= supply[i] * y[i]
)

# Koppling flöde–betjäning
@constraint( mod, c3[(i,j) in KOMB],
    x[(i,j)] <= demand[j] * z[(i,j)]
)

# Max antal detaljister per lager
@constraint( mod, c4[i in L],
    sum( z[(i,j)] for j in D if (i,j) in KOMB ) <= K * y[i]
)

#----------------------------------------------------------------

#-----------------  STUDENT PART END  ----------------



#-------------------------------------------------------------------------------
# Skriv ut modell-objektet
#-------------------------------------------------------------------------------
println(repeat('-',60))
println("Modellen")
println(repeat('-',60))
print(mod)
println(repeat('-',60))
#-------------------------------------------------------------------------------


#-------------------------------------------------------------------------------
# Lös problemet
#-------------------------------------------------------------------------------
println("\n\n\n>>> LÖS OPTIMERINGSSPROBLEM <<<\n")
optimize!(mod)
#-------------------------------------------------------------------------------


